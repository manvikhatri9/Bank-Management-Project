    private void monthCBItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_monthCBItemStateChanged
        int index = monthCB.getSelectedIndex();
        dayCB.setEnabled(false);
        dayCB.setModel(new DefaultComboBoxModel(new String[]{"Select Day"}));
        if(index !=0){
            dayCB.setEnabled(true);
            boolean res = checkLeapYear(Integer.parseInt((String) yearCB.getSelectedItem()));
            if(index == 2){
                if(res == true){
                    dayCB.setModel(new DefaultComboBoxModel(day_29));
                }
                else{
                    dayCB.setModel(new DefaultComboBoxModel(day_28));
                }
            }
            if(index==1||index==3|| index==5|| index==7||index==8||index==10|| index==12){
                    dayCB.setModel(new DefaultComboBoxModel(day_31));
                }
            if(index==4||index==6|| index==9|| index==11){
                    dayCB.setModel(new DefaultComboBoxModel(day_30));
                }
        }
    }//GEN-LAST:event_monthCBItemStateChanged

